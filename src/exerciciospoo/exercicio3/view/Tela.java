    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
       String nome = campoNome.getText();
       
       int idade = 0;
       try {
           idade = Integer.parseInt(campoIdade.getText());
        } catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Digite apenas n√∫meros no campo Ano!", 
                                  "Erro de entrada", JOptionPane.ERROR_MESSAGE);
        }
       String matricula = (String) campoMatricula.getSelectedItem();
       String tipo = (String) campoTipo.getSelectedItem();
       
       if (tipo.equalsIgnoreCase("pessoa")) {
           Pessoa p = new Pessoa(nome, idade);
           dados.add(p);
       } else if(tipo.equalsIgnoreCase("aluno")) {
           Aluno a = new Aluno (nome, idade, matricula);// TODO add your handling code he
           dados.add(a);
       }
       atualizarTabela();
       
       try {
            Ficheiro1.gravar("pessoas.dat", dados);
        } catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro ao salvar: " + e.getMessage());
        }
       
       campoNome.setText("");
       campoIdade.setText("");
    }//GEN-LAST:event_botaoSalvarActionPerformed

